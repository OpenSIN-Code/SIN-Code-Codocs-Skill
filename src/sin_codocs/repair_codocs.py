#!/usr/bin/env python3
"""Repair CoDocs references in a directory.

Two repair modes (idempotent, safe to re-run):

  1. MISSING-COMPANION: code file declares `Docs: x.doc.md` but the
     referenced file does not exist -> create a draft skeleton.

  2. WRONG-HEADER-PATH: code file declares `Docs: <subdir>/x.doc.md`
     but the file lives at the parent directory (i.e. the header has
     an extra path component) -> rewrite the header to the correct
     relative path.

Usage:
    python3 repair_codocs.py <skill-dir> [--dry-run]
"""
from pathlib import Path
import argparse
import re
import sys

HEAD_LINES = 5
CODE_SUFFIXES = {
    ".py", ".pyi", ".ts", ".tsx", ".js", ".jsx", ".mjs", ".cjs",
    ".rs", ".go", ".java", ".kt", ".kts", ".scala",
    ".c", ".h", ".cc", ".cpp", ".hpp", ".cs", ".rb", ".php",
    ".swift", ".sh", ".bash", ".zsh", ".yaml", ".yml", ".toml",
}
CODE_FILENAMES = {"Makefile", "Dockerfile", "Justfile"}
EXCLUDE = {
    ".git", "__pycache__", "node_modules", "venv", ".venv",
    "dist", "build", ".mypy_cache", ".pytest_cache", ".ruff_cache",
    "site-packages", ".egg-info",
}
DOCS_RE = re.compile(
    r"^(\s*(?:#|//|/\*|\*|--|;)?\s*)Docs:\s*(?P<doc>[^\s*]+?\.doc\.md)"
)

SKELETON_TEMPLATE = """# {filename}

> **Status: DRAFT** — auto-generated by `repair_codocs.py`. Every
> `<TODO: …>` block needs a human to fill it in.

**Purpose:** <TODO: one-line summary of what this file does>

**Source file:** `{rel_path}` ({language})

**Header excerpt:**

```
{header}
```

---

## What it does

<TODO: 1-paragraph elevator pitch. What is this file responsible for?
What would break if it were deleted?>

## Dependencies

<TODO: list the key imports / dependencies and WHY each is used>

## Important config

<TODO: magic numbers, env vars, config keys with units / constraints>

## Why these decisions

<TODO: non-obvious choices (why NOT the obvious approach)>

## Usage example

```bash
<TODO: 1-3 line usage example>
```

## Known caveats

<TODO: footguns, edge cases, security notes>
"""


def _iter_code_files(root: Path):
    for path in sorted(root.rglob("*")):
        if not path.is_file():
            continue
        if any(part in EXCLUDE for part in path.parts):
            continue
        if path.name in CODE_FILENAMES or path.suffix in CODE_SUFFIXES:
            yield path


def _find_docs_line(path: Path) -> tuple[int, re.Match] | None:
    """Return (line_index, match) for the first Docs: line in the head."""
    try:
        with path.open("r", encoding="utf-8", errors="ignore") as fh:
            for i in range(HEAD_LINES):
                line = fh.readline()
                if line == "":
                    return None
                m = DOCS_RE.match(line)
                if m:
                    return (i, m)
    except OSError:
        return None
    return None


def _read_lines(path: Path, n: int) -> str:
    try:
        with path.open("r", encoding="utf-8", errors="ignore") as fh:
            return "".join(fh.readline() for _ in range(n))
    except OSError:
        return ""


def main() -> int:
    ap = argparse.ArgumentParser()
    ap.add_argument("skill_dir")
    ap.add_argument("--dry-run", action="store_true")
    args = ap.parse_args()

    root = Path(args.skill_dir).resolve()
    if not root.is_dir():
        print(f"Not a directory: {root}", file=sys.stderr)
        return 1

    fixed_header = 0
    created = 0
    skipped = 0
    errors: list[str] = []

    for path in _iter_code_files(root):
        rel = path.relative_to(root)
        found = _find_docs_line(path)
        if found is None:
            continue
        line_idx, match = found
        declared = match.group("doc")
        companion = (path.parent / declared).resolve()
        if companion.is_file():
            skipped += 1
            continue

        # Companion missing. Two possibilities:
        #   (a) declared path is wrong (extra subdir) but file exists in parent
        #   (b) declared path is correct but file does not exist (needs skeleton)

        candidate_basenames = []
        # Try stripping leading subdir components
        for n in range(declared.count("/") + 1):
            candidate_basenames.append("/".join(declared.split("/")[n:]))
        # Also try replacing the leading subdir with path.parent.name
        if "/" in declared:
            subdir, rest = declared.split("/", 1)
            candidate_basenames.append(rest)
            candidate_basenames.append(f"{path.parent.name}/{rest}")

        found_alternative = None
        for cand in candidate_basenames:
            cand_path = (path.parent / cand).resolve()
            if cand_path.is_file():
                found_alternative = cand
                break

        if found_alternative and found_alternative != declared:
            # Mode 1: fix the header in-place
            if args.dry_run:
                print(f"  [fix-hdr] {rel}: {declared!r} -> {found_alternative!r}")
            else:
                try:
                    with path.open("r", encoding="utf-8", errors="ignore") as fh:
                        lines = fh.readlines()
                    line = lines[line_idx]
                    new_line = DOCS_RE.sub(
                        lambda m: m.group(1) + "Docs: " + found_alternative,
                        line,
                        count=1,
                    )
                    lines[line_idx] = new_line
                    with path.open("w", encoding="utf-8") as fh:
                        fh.writelines(lines)
                    print(f"  [fix-hdr] {rel}: {declared!r} -> {found_alternative!r}")
                except OSError as e:
                    errors.append(f"{rel}: {e}")
                    continue
            fixed_header += 1
            continue

        # Mode 2: create skeleton for missing companion
        if path.suffix in (".py", ".pyi"):
            language = "Python"
        elif path.suffix:
            language = path.suffix.lstrip(".").upper()
        else:
            language = "Shell" if path.name in CODE_FILENAMES else "Config"

        header = _read_lines(path, 8)
        skeleton = SKELETON_TEMPLATE.format(
            filename=path.name,
            rel_path=rel,
            language=language,
            header=header,
        )
        if args.dry_run:
            print(f"  [create]  {companion.relative_to(root)}")
        else:
            try:
                companion.parent.mkdir(parents=True, exist_ok=True)
                companion.write_text(skeleton, encoding="utf-8")
                print(f"  [create]  {companion.relative_to(root)}")
            except OSError as e:
                errors.append(f"{rel}: {e}")
                continue
        created += 1

    print(
        f"\nDone. fixed_header={fixed_header}, created={created}, "
        f"skipped={skipped}, errors={len(errors)}"
    )
    for e in errors:
        print(f"  ! {e}")
    return 0 if not errors else 2


if __name__ == "__main__":
    raise SystemExit(main())
